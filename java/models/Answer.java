package models;

public class Answer {
    
    String aid;
    String value;
    int avotes;

    public String getAid() {
        return this.aid;
    }

    public void setAid(String aid) {
        this.aid = aid;
    }

    public String getValue() {
        return this.value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getAvotes() {
        return this.avotes;
    }

    public void setAvotes(int avotes) {
        this.avotes = avotes;
    } 
}